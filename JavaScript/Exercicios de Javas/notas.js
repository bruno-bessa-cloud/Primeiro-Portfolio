let media = 300;

if (media <= 100 && media >= 90){
    console.log("A");
} else if (media <= 89 && media >= 70){
    console.log("B");
} else if (media <= 69 && media >= 60){
    console.log("C");
} else if (media <= 59 && media >= 50){
    console.log("D");
} else if (media <= 49 && media >= 0){
    console.log("F");
} else {
    console.log("Pontuação Invalida");
}